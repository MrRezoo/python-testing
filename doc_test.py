def add(x, y):
    """
    >>> add(3, 4)
    7
    >>> add(4, -1)
    3
    >>> add(0, 5)
    5
    """
    return x + y


def subtract(x, y):
    """
    >>> subtract(3, 4)
    -1
    >>> subtract(4, -1)
    5
    >>> subtract(6, 5)
    1
    """
    return x - y


def multiply(x, y):
    """
      >>> multiply(2, 4)
      8
      >>> multiply(4, -1)
      -4
      >>> multiply(2, 'reza')
      'rezareza'
    """
    return x * y